package com.kniffel.tableCell;

public class TableCell {
    private int row;
    private int col;

    public TableCell() {
    }

    public int getRow() {
        return row;
    }
    public void setRow(int row) {
        this.row = row;
    }
    public int getCol() {
        return col;
    }
    public void setCol(int col) {
        this.col = col;
    }


}
